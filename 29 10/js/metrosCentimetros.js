//As variaveis servem para pegar o numero que o usuario deseja e converter em centimetro
var metro = parseFloat(prompt("Informe os metros que deseja converter: "));
var centimetro = metro * 100;

//As linhas abaixo serve para alertar o usuario o resoltado e apresentar no console
alert("Metros: " + metro + "\nCentimetro: " + centimetro);
console.log("Metros: " + metro + "\nCentimetro: " + centimetro);