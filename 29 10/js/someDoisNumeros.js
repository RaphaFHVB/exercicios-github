//As duas variaveis servem para pegar um numero solicitado pelo usuario
var number1 = parseFloat(prompt("Informe o primeiro número que iremos somar: "));
var number2 = parseFloat(prompt("Informe o segundo número que iremos somar: "));
//Esta variavel serve para somar os numeros informados
var soma = number1 + number2;
//Abaixo serve para alertar o resultado da soma dos numeros que o usuario informou
alert("A soma dos números que você informou é: " + soma);
//Abaixo serve para mostrar o resultado da soma no console
console.log("A soma dos números que você informou é: " + soma);